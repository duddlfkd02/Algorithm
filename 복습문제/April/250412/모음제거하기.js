let s = "Hello World";

function solution(s) {
  const vowel = ["a", "e", "i", "o", "u", "A", "E", "I", "O", "U"];

  return s
    .split("")
    .filter((str) => !vowel.includes(str))
    .join("");
}
solution(s);


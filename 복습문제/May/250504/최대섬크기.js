function solution(grid) {
  const N = grid.length;
  const M = grid[0].length;
  // visited[i][j] = true ë©´ (i,j) ë•…ì„ ì´ë¯¸ ì„¸ì–´ë´¤ë‹¤ëŠ” ëœ»
  const visited = Array.from({ length: N }, () => Array(M).fill(false));

  const dx = [1, -1, 0, 0];
  const dy = [0, 0, 1, -1];

  function dfs(i, j) {
    // 1. ë²”ìœ„ ë°–ì´ê±°ë‚˜, ë¬¼(0)ì´ê±°ë‚˜, ì´ë¯¸ ë°©ë¬¸í•œ ê²½ìš°
    if (i < 0 || j < 0 || i >= N || j >= M || grid[i][j] === 0 || visited[i][j])
      return 0;

    // 2. í˜„ì¬ ë•… ë°©ë¬¸ ì²˜ë¦¬
    visited[i][j] = true;

    // 3. ì¹¸ í•˜ë‚˜ì˜ ë„“ì´ 1 ë”í•˜ê¸°
    let area = 1;

    // 4. ë„¤ ë°©í–¥ìœ¼ë¡œ ì´ì–´ì§„ ë•…ì„ ì¬ê·€ì ìœ¼ë¡œ í˜¸ì¶œ
    for (let k = 0; k < 4; k++) {
      const ni = i + dx[k];
      const nj = j + dy[k];
      area += dfs(ni, nj);
    }
    return area;
  }

  let maxArea = 0;

  for (let i = 0; i < N; i++) {
    for (let j = 0; j < M; j++) {
      if (grid[i][j] === 1 && !visited[i][j]) {
        const current = dfs(i, j);
        maxArea = Math.max(maxArea, current);
      }
    }
  }
  return maxArea;
}


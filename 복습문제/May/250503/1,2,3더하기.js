const N = 4;

function solution(N) {
  const dp = Array.from({ length: N + 1 }, () => Array(0));
  console.log(dp);

  dp[0] = 1;
  if (N >= 1) dp[1] = 1;
  if (N >= 2) dp[2] = 2;

  for (let i = 3; i <= N; i++) {
    dp[i] = dp[i - 1] + dp[i - 2] + dp[i - 3];
  }

  return dp[N];
}

const arr1 = [1, 3, 4, 5, 7];
const arr2 = [3, 5, 6, 7, 8];

function solution(arr1, arr2) {
  const set1 = new Set(arr1);
  const set2 = new Set(arr2);

  const result = [...set1].filter((num) => set2.has(num));

  return result.sort((a, b) => a - b);
}

